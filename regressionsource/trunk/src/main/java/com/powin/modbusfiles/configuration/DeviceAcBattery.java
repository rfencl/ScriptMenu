package com.powin.modbusfiles.configuration;

import java.util.ArrayList;
import java.util.List;

public class DeviceAcBattery {
	public DeviceAcBattery(int arrayIndex, List<Integer> pcsIndexes, boolean enabled,  int stopPriority,int startPriority) {
		super();
		this.arrayIndex = arrayIndex;
		this.pcsIndexes=pcsIndexes;
		this.enabled = enabled;
		this.stopPriority = stopPriority;
		this.startPriority = startPriority;
	}

	private int arrayIndex;
	private List<Integer> pcsIndexes;
	private boolean enabled;
	private int stopPriority;
	private int startPriority;
	
	public int getArrayIndex() {
		return arrayIndex;
	}
	public void setArrayIndex(int arrayIndex) {
		this.arrayIndex = arrayIndex;
	}
	public List<Integer> getPcsIndexes() {
		return pcsIndexes;
	}
	public void setPcsIndexes(List<Integer> pcsIndexes) {
		this.pcsIndexes = pcsIndexes;
	}
	public boolean isEnabled() {
		return enabled;
	}
	public void setEnabled(boolean enabled) {
		this.enabled = enabled;
	}
	public int getStopPriority() {
		return stopPriority;
	}
	public void setStopPriority(int stopPriority) {
		this.stopPriority = stopPriority;
	}
	public int getStartPriority() {
		return startPriority;
	}
	public void setStartPriority(int startPriority) {
		this.startPriority = startPriority;
	}
}
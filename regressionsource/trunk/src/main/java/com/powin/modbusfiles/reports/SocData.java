package com.powin.modbusfiles.reports;

import com.fasterxml.jackson.annotation.JsonProperty;

public class SocData {
	private int stackCount;

	@JsonProperty(value = "kWh")
	private double kWh;

	@JsonProperty(value = "kWhCapacity")
	private int kWhCapacity;

	private int soc;

	private int dod;

	@JsonProperty(value = "Ah")
	private double ah;

	@JsonProperty(value = "AhCapacity")
	private int ahCapacity;

	public void setStackCount(int stackCount) {
		this.stackCount = stackCount;
	}

	public int getStackCount() {
		return this.stackCount;
	}

	public void setKWh(double kWh) {
		this.kWh = kWh;
	}

	public double getKWh() {
		return this.kWh;
	}

	public void setKWhCapacity(int kWhCapacity) {
		this.kWhCapacity = kWhCapacity;
	}

	public int getKWhCapacity() {
		return this.kWhCapacity;
	}

	public void setSoc(int soc) {
		this.soc = soc;
	}

	public int getSoc() {
		return this.soc;
	}

	public void setDod(int dod) {
		this.dod = dod;
	}

	public int getDod() {
		return this.dod;
	}

	public void setAh(double ah) {
		this.ah = ah;
	}

	public double getAh() {
		return this.ah;
	}

	public void setAhCapacity(int ahCapacity) {
		this.ahCapacity = ahCapacity;
	}

	public int getAhCapacity() {
		return this.ahCapacity;
	}
}
